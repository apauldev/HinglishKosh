    """Test romanization of 500 additional common Hindi words."""

    @pytest.mark.parametrize(
        "hindi,expected",
        sorted(EXTRA_WORDS_500.items()),
        ids=[f"{k}→{v}" for k, v in sorted(EXTRA_WORDS_500.items())],
    )
    def test_romanization(self, hindi: str, expected: str) -> None:
        result = transliterate_rule_based(hindi)
        assert result == expected, (
            f"transliterate_rule_based({hindi!r}) = {result!r}, expected {expected!r}"
        )

    def test_word_count(self) -> None:
        """Verify we have at least 500 words."""
