        """Verify no Devanagari characters in expected values."""
        for hindi, expected in EXTRA_WORDS_500.items():
            for c in expected:
                assert not ("\u0900" <= c <= "\u097f"), (
                    f"{hindi}: expected {expected!r} contains Devanagari char {c!r}"
                )

