        """Verify all expected values are ASCII lowercase."""
        for hindi, expected in EXTRA_WORDS_500.items():
            assert expected.isascii(), f"{hindi}: expected {expected!r} is not ASCII"
            assert expected == expected.lower(), f"{hindi}: expected {expected!r} is not lowercase"

