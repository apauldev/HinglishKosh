        """Verify no diacritical marks in expected values."""
        import unicodedata
        for hindi, expected in EXTRA_WORDS_500.items():
            for c in expected:
                cat = unicodedata.category(c)
                assert cat != "Mn", (
                    f"{hindi}: expected {expected!r} contains combining mark {c!r}"
                )

