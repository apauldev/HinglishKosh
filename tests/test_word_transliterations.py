class TestNoDiacritics:
    DIACRITICS = set(
        "\u0101\u012b\u016b\u0113\u014d\u1e43\u1e25\u1e63\u015b\u1e6d\u1e0d\u1e47\u1e45\u00f1"
    )  # noqa: E501

    def test_no_diacritics(self, dictionary):
        sample = random.sample(dictionary, min(1000, len(dictionary)))
        for entry in sample:
            roman = entry.get("word_hinglish_roman", "")
            assert not any(ch in self.DIACRITICS for ch in roman), (  # noqa: E501
                f"{entry.get('word_hindi')}: {roman}"
            )


class TestAccuracy:
    def test_accuracy_stats(self):
        passed = len(EXTRA_WORDS_500_PASS)
        total = passed + len(EXTRA_WORDS_500_XFAIL)
        pct = passed * 100 / total
        print(f"\n  Engine accuracy: {passed}/{total} = {pct:.1f}%")
        print(f"  Known gaps: {len(EXTRA_WORDS_500_XFAIL)} words (schwa deletion)")
        print(f"  To reach 90%: add {max(0, int(total * 0.9) - passed)} words to common_words.json")
        assert passed >= 300, f"Accuracy regressed ({passed}/500)"
